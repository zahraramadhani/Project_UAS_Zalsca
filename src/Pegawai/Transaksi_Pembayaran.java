private void loadData() {
    try {
        Connection conn = DatabaseConnection.getConnection();
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM transaksi_pembayaran");

        // Kosongkan tabel terlebih dahulu
        model.setRowCount(0);

        while (rs.next()) {
            String id = rs.getString("id_transaksi");
            String kode = rs.getString("kode_barang");
            String nama = rs.getString("nama_barang");
            int harga = rs.getInt("harga_jual");
            int jumlah = rs.getInt("jumlah");
            String jenis = rs.getString("jenis_pembayaran");
            int total = rs.getInt("total");
            String tanggal = rs.getString("tanggal_transaksi");

            model.addRow(new Object[]{id, kode, nama, harga, jumlah, jenis, total, tanggal});
        }

        rs.close();
        stmt.close();
        conn.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data dari database.\n" + e.getMessage());
    }
}
