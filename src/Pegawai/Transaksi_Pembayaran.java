    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String kode = jTextField2.getText();
        String nama = (String) jComboBox1.getSelectedItem();
        String hargaStr = jTextField3.getText();
        String jumlahStr = jTextField4.getText();
        String jenis = (String) jComboBox2.getSelectedItem();
        String tanggalStr = jTextField6.getText();

        if (id.isEmpty() || kode.isEmpty() || nama == null || hargaStr.isEmpty() || jumlahStr.isEmpty() || jenis == null || tanggalStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon lengkapi semua data!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int harga = Integer.parseInt(hargaStr);
            int jumlah = Integer.parseInt(jumlahStr);
            int total = harga * jumlah;
            jTextField5.setText(String.valueOf(total));

            // Konversi tanggal dari dd/MM/yyyy ke yyyy-MM-dd
            SimpleDateFormat sdfInput = new SimpleDateFormat("dd/MM/yyyy");
            SimpleDateFormat sdfOutput = new SimpleDateFormat("yyyy-MM-dd");
            Date tanggal = sdfInput.parse(tanggalStr);
            String tanggalSQL = sdfOutput.format(tanggal);
            
            Connection conn = DatabaseConnection.getConnection();
            String sqlCekStok = "SELECT stok FROM stok_barang WHERE kode_barang = ?";
            PreparedStatement pstCekStok = conn.prepareStatement(sqlCekStok);
            pstCekStok.setString(1, kode);
            ResultSet rsCek = pstCekStok.executeQuery();

            if (rsCek.next()) {
                int stokTersedia = rsCek.getInt("stok");
                if (jumlah > stokTersedia) {
                    JOptionPane.showMessageDialog(this, "Stok tidak mencukupi untuk transaksi ini!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Data stok barang tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String sql = "INSERT INTO transaksi_pembayaran "
           + "(kode_barang, nama_barang, harga_jual, jumlah, "
           + " jenis_pembayaran, total, tanggal_transaksi) "
           + "VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, kode);
            stmt.setString(2, nama);
            stmt.setInt(3, harga);
            stmt.setInt(4, jumlah);
            stmt.setString(5, jenis);
            stmt.setInt(6, total);
            stmt.setString(7, tanggalSQL);
            
            String sqlPenjualan = "INSERT INTO transaksi_penjualan "
            + "(kode_barang, nama_barang, harga_jual, jumlah, total, tanggal_transaksi) "
            + "VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pstPenj = conn.prepareStatement(sqlPenjualan);
            pstPenj.setString(1, kode);
            pstPenj.setString(2, nama);
            pstPenj.setInt(3, harga);
            pstPenj.setInt(4, jumlah);
            pstPenj.setInt(5, total);
            pstPenj.setString(6, tanggalSQL);
            pstPenj.executeUpdate();
            
            String sqlUpdateStok = "UPDATE stok_barang SET stok = stok - ? WHERE kode_barang = ?";
            PreparedStatement pstStok = conn.prepareStatement(sqlUpdateStok);
            pstStok.setInt(1, jumlah); // jumlah barang yang dibeli
            pstStok.setString(2, kode);
            pstStok.executeUpdate();

            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                model.addRow(new Object[]{id, kode, nama, harga, jumlah, jenis, total, tanggalSQL});
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database.");
            }
           
            // Reset form
            jTextField1.setText("");
            jTextField2.setText("");
            jComboBox1.setSelectedIndex(-1);
            jTextField3.setText("");
            jTextField4.setText("");
            jComboBox2.setSelectedIndex(-1);
            jTextField5.setText("");
            jTextField6.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga dan Jumlah harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Format tanggal salah. Gunakan format dd/MM/yyyy", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

